# Sum of Natural Numbers
# Write a Python program that calculates the sum of the first 50 natural numbers using a for loop.

# Initialize a variable to store the sum
sum_of_numbers = 0

# Use a for loop to iterate through the first 50 natural numbers
for num in range(1, 51):
    sum_of_numbers += num

# Print the result
print(f"The sum of the first 50 natural numbers is: {sum_of_numbers}")
